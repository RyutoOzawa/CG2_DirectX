#pragma once

#include"FbxModel.h"
#include"Camera.h"

#include<Windows.h>
#include<wrl.h>
#include<d3d12.h>
#include<d3dx12.h>
#include<DirectXMath.h>
#include<string>
#include"Vector3.h"
#include"Matrix4.h"

class FbxObject3d
{
protected:
	//Microsoft::WRL::を省略
	template<class T>using ComPtr = Microsoft::WRL::ComPtr<T>;

public://静的メンバ関数
	//setter
	static void SetDevice(ID3D12Device* device_) { device = device_; }
	static void SetCmdList(ID3D12GraphicsCommandList* cmdList_) { cmdList = cmdList_; }
	static void SetCamera(Camera* camera_) { camera = camera_; }

	/// <summary>
	/// グラフィックスパイプラインの生成
	/// </summary>
	static void CreateGraphicsPipeline();

private://静的メンバ変数
	//デバイス
	static ID3D12Device* device;
	//コマンドリスト
	static ID3D12GraphicsCommandList* cmdList;
	//カメラ
	static Camera* camera;
	//ルートシグネチャ
	static ComPtr<ID3D12RootSignature> rootsignature;
	//パイプラインステートオブジェクト
	static ComPtr<ID3D12PipelineState> pipelinestate;

public://定数
	//ボーンの最大数
	static const int MAX_BONES = 32;



public://サブクラス

	//定数バッファ用データ構造体
	struct ConstBufferDataTransform {
		Matrix4 viewProjection;	//ビュープロジェクション行列
		Matrix4 world;			//ワールド行列
		Vector3 cameraPos;		//カメラ座標(ワールド座標)
	};

	//定数バッファ用データ構造体(スキニング)
	struct ConstBufferDataSkin {
		Matrix4 bones[MAX_BONES];
	};

public://メンバ関数

	/// <summary>
	/// 初期化
	/// </summary>
	void Initialize();

	/// <summary>
	/// 毎フレーム処理
	/// </summary>
	void Update();

	/// <summary>
	/// モデルのセット
	/// </summary>
	/// <param name="fbxModel">fbxモデルデータ</param>
	void SetModel(FbxModel* fbxModel_) { fbxModel = fbxModel_; }

	/// <summary>
	/// 描画
	/// </summary>
	void Draw();

	/// <summary>
	/// アニメーション開始
	/// </summary>
	void PlayAnitimation();

	void SetRot(const Vector3& rotation_) { rotation = rotation_; }

	FbxTime BaGetCurrentTime() { return currentTime; }

protected://メンバ変数
	//定数バッファ
	ComPtr<ID3D12Resource> constBuffTransform;
	//定数バッファ(スキン)
	ComPtr<ID3D12Resource> constBuffSkin;

	//ローカルスケール
	Vector3 scale = { 1,1,1 };
	//X,Y,Z軸周りのローカル回転角
	Vector3 rotation = { 0,0,0 };
	//ローカル座標
	Vector3 position = { 0,0,0 };
	//ローカルワールド変換行列
	Matrix4 matWorld;
	//モデル
	FbxModel* fbxModel = nullptr;

	//1フレームの時間
	FbxTime frameTime;
	//アニメーション開始時間
	FbxTime startTime;
	//アニメーション終了時間
	FbxTime endTime;
	//現在時間(アニメーション)
	FbxTime currentTime;
	//アニメーション再生中
	bool isPlay = false;
};

